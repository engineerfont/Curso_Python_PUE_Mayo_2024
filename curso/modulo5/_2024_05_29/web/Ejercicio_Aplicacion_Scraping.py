# Crear una aplicación que dada una url se obtenga lo siguiente:

# - Una lista con todos los enlaces de la etiqueta <a>
#   EL enlace está en el atributo 'href' de la etiqueta
#   Ejemplo <a href='https://www.pue.es/'>PUE</a>

# - Una lista con todas las imágenes de la etiqueta <img>
#   EL enlace está en el atributo 'src' de la etiqueta
#   Ejemplo <img src='https://www.pue.es/images/logo.png'>

# - Una lista con todas las hojas de estilo de la etiqueta <link>
#   EL enlace está en el atributo 'href' de la etiqueta
#   Ejemplo <link rel='stylesheet' href='https://www.pue.es/css/styles.css'>

# - Una lista con todos los archivos JavaScript de la etiqueta <script>
#   EL enlace está en el atributo 'src' de la etiqueta
#   <script src="https://www.pue.es/resources/scripts/menus/menu.js"></script>

